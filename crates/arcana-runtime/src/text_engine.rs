pub(crate) const DEFAULT_FONT_SIZE: i64 = 16;

pub(crate) trait TextPaintSink {
    fn fill_rect(&mut self, x: i64, y: i64, w: i64, h: i64, color: i64);
}

pub(crate) fn measure_plain_text(text: &str, font_size: i64) -> (i64, i64) {
    let line_height = glyph_cell_height(font_size);
    let mut current = 0;
    let mut longest = 0;
    let mut lines = 1;
    for ch in text.chars() {
        if ch == '\n' {
            longest = longest.max(current);
            current = 0;
            lines += 1;
            continue;
        }
        current += glyph_advance(font_size, ch);
    }
    longest = longest.max(current);
    (longest, line_height * lines)
}

pub(crate) fn paint_plain_text<S: TextPaintSink>(
    sink: &mut S,
    x: i64,
    y: i64,
    text: &str,
    color: i64,
    font_size: i64,
) {
    let mut cursor_x = x;
    let mut cursor_y = y;
    let line_height = glyph_cell_height(font_size);
    for ch in text.chars() {
        if ch == '\n' {
            cursor_x = x;
            cursor_y += line_height;
            continue;
        }
        paint_glyph(sink, cursor_x, cursor_y, ch, font_size, color);
        cursor_x += glyph_advance(font_size, ch);
    }
}

fn glyph_advance(font_size: i64, ch: char) -> i64 {
    let base = glyph_cell_width(font_size);
    let extra = if ch == ' ' { base / 2 } else { 0 };
    (base + extra).max(1)
}

fn glyph_cell_width(font_size: i64) -> i64 {
    ((font_size.max(8) * 8) + 15) / 16
}

fn glyph_cell_height(font_size: i64) -> i64 {
    font_size.max(8)
}

fn paint_glyph<S: TextPaintSink>(
    sink: &mut S,
    x: i64,
    y: i64,
    ch: char,
    font_size: i64,
    color: i64,
) {
    let glyph = glyph_rows(ch);
    let cell_w = glyph_cell_width(font_size);
    let cell_h = glyph_cell_height(font_size);
    for (row, bits) in glyph.into_iter().enumerate() {
        let row = i64::try_from(row).unwrap_or(0);
        let py0 = y + (row * cell_h) / 8;
        let py1 = y + (((row + 1) * cell_h) / 8).max((row * cell_h) / 8 + 1);
        for col in 0..8i64 {
            if bits & (1 << col) == 0 {
                continue;
            }
            let px0 = x + (col * cell_w) / 8;
            let px1 = x + (((col + 1) * cell_w) / 8).max((col * cell_w) / 8 + 1);
            sink.fill_rect(px0, py0, px1 - px0, py1 - py0, color);
        }
    }
}

fn glyph_rows(ch: char) -> [u8; 8] {
    let idx = ch as usize;
    if idx < BASIC_ASCII_GLYPHS.len() {
        BASIC_ASCII_GLYPHS[idx]
    } else {
        BASIC_ASCII_GLYPHS[b'?' as usize]
    }
}

